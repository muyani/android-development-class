package com.letina;

public class DayThreeLoops {
    public static void main(String[] args) {
        int x=3;
        int y=20;
        //while loop
        while (x<5){
            System.out.println(x);
           x++;
        }

        //Do while loop
        do{
            System.out.println(y);
            y++;
        }while(y<15);
        
    }
}
